#####DrugThatGene is a docker-based tool! Not R.#####

To use it, make sure you have the latest docker version installed (https://docs.docker.com/engine/installation/)

Then, in the command terminal, type in:
docker run -p 9999:9999 -it pinellolab/drugthatgene

Then, go to your web browser, e.g. Chrome, and type this in address bar:

localhost:9999

From there, you can just type in the genes of your interest. 

